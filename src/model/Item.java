package model;

public class Item {

	protected String itemCode;
	protected String modelName;
	protected String itemCondition;
	protected String itemStatus;
	protected String idOrderSupplier;
	
	
	public String getItemCode() {
		return itemCode;
	}
	public void setItemCode(String itemCode) {
		this.itemCode = itemCode;
	}
	public String getModelName() {
		return modelName;
	}
	public void setModelName(String modelName) {
		this.modelName = modelName;
	}
	public String getItemCondition() {
		return itemCondition;
	}
	public void setItemCondition(String itemCondition) {
		this.itemCondition = itemCondition;
	}
	public String getItemStatus() {
		return itemStatus;
	}
	public void setItemStatus(String itemStatus) {
		this.itemStatus = itemStatus;
	}
	public String getIdOrderSupplier() {
		return idOrderSupplier;
	}
	public void setIdOrderSupplier(String idOrderSupplier) {
		this.idOrderSupplier = idOrderSupplier;
	}
	
	
}
